#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum Language {
    En,
    Ru,
}

impl Language {
    pub fn toggle(self) -> Self {
        match self {
            Self::En => Self::Ru,
            Self::Ru => Self::En,
        }
    }

    pub fn code(self) -> &'static str {
        match self {
            Self::En => "EN",
            Self::Ru => "RU",
        }
    }
}

